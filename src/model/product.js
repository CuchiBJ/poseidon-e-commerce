export class Product {

  constructor(id, description, brand, price, buyPrice, name, supplier, size, color){
    this.id = id;
    this.description = description;
    this.brand = brand;
    this.price = price;
    this.buyPrice = buyPrice;
    this.name = name;
    this.supplier = supplier;
    this.size = size;
    this.color = color;
  }

}